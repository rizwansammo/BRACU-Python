# Variable assignment
x = 10          # Integer
y = 3.14        # Float
name = "Rizwan" # String
is_student = True # Boolean

# Printing variables
print(x)        # Output: 10
print(y)        # Output: 3.14
print(name)     # Output: Rizwan
print(is_student) # Output: True

# Dynamic typing
x = "Now I'm a string"
print(x)        # Output: Now I'm a string